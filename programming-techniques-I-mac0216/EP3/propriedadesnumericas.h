/*!
* \fn int ehPar (long long)
* \brief Verifica se um número é par ou não.
*
* \param x (long long): número que vai ter a paridade testata.
* \return retorno (int): 1 se o número é par. 0 se for ímpar. 
*/

/*!
* \fn int ehPrimo (long long)
* \brief Verifica se um número é primo ou não.
*
* \param x (long long): número sobre o qual será verificado se é primo.
* \return retorno (int): 1 se o número é primo. 0 caso contrário.
*/

/*!
* \fn int ehQuadradoPerfeito (long long)
* \brief Verifica se um número é quadrado ou não.
*
* \param x (long long): número o qual será tirada a raiz quadrada e porteriormente testar se o resultado ao quadrado é igual ao número de entrada
* \return retorno (int): 1 se o número é quadrado perfeito. 0 caso contrário.
*/

/*!
* \fn int ehCuboPerfeito (long long)
* \brief Verifica se um número é cubo perfeito ou não.
*
* \param x (long long): número o qual será tirada a raiz cúbica e porteriormente testar se o resultado ao cubo é igual ao número de entrada
* \return retorno (int): 1 se o número é cubo perfeito. 0 caso contrário.
*/

/*!
* \fn int ehFibonacci (long long)
* \brief Verifica se um número é de Fibonacci ou não.
*
* \param x (long long): número sobre o qual será verificado se é um número de Fibonacci.
* \return retorno (int): 1 se o número é fibonacci. 0 caso contrário.
*/

/*!
* \fn int ehFatorial (long long)
* \brief Verifica se um número é fatorial ou não.
*
* \param x (long long): número sobre o qual será verificado se é um número fatorial.
* \return retorno (int): 1 se o número é fatorial. 0 caso contrário.
*/

int ehPar (long long x);
int ehPrimo (long long x);
int ehQuadradoPerfeito (long long x);
int ehCuboPerfeito (long long x);
int ehFibonacci (long long x);
